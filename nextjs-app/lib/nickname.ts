export function generateKoreanNickname(address: `0x${string}`): string {
  const hash = keccak256(address);
  const hashNum = BigInt(hash);
  const adjIndex = Number(hashNum % BigInt(adjectives.length));
  const nounIndex = Number(
    (hashNum / BigInt(adjectives.length)) % BigInt(yugiohTheme.length)
  );

  return `${adjectives[adjIndex]} ${yugiohTheme[nounIndex]}`;
}
